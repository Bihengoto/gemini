/*
Day 7: Object-Oriented Programming (OOP) - Core Principles

Object-Oriented Programming is a programming paradigm based on the concept of "objects",
which can contain data (attributes) and code (methods).

The four main principles of OOP are:

1. ENCAPSULATION
   - Bundling data and methods that work on that data within one unit (class)
   - Hiding internal implementation details from the outside world
   - Using private variables and public methods to control access

2. INHERITANCE
   - Creating new classes based on existing classes
   - Child classes inherit properties and methods from parent classes
   - Promotes code reusability and establishes relationships between classes

3. POLYMORPHISM
   - The ability of objects of different types to be treated as instances of the same type
   - Same method name can have different implementations in different classes
   - Method overriding and method overloading

4. ABSTRACTION
   - Hiding complex implementation details and showing only essential features
   - Creating abstract classes and interfaces
   - Focusing on what an object does rather than how it does it

Benefits of OOP:
- Code reusability
- Modularity and organization
- Easier maintenance and debugging
- Real-world modeling
- Scalability
*/

